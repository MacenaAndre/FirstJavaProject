public class College {
    private String name;
    private String state;
    private String city;

    String getName() {
        return this.name;
    }

    void setName(String name) {
        this.name = name;
    }

    String getState() {
        return this.state;
    }

    void setState(String state) {
        this.state = state;
    }

    String getCity() {
        return this.city;
    }

    void setCity(String city) {
        this.city = city;
    }
}
